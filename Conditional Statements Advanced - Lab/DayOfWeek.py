weekDay = int(input())

if weekDay == 1:
    print("Monday")
elif weekDay == 2:
    print("Tuesday")
elif weekDay == 3:
    print("Wednesday")
elif weekDay == 4:
    print("Thursday")
elif weekDay == 5:
    print("Friday")
elif weekDay == 6:
    print("Saturday")
elif weekDay == 7:
    print("Sunday")
else:
    print("Error")